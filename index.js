/**
 * Run single period on given matrix data
 * @param {Array} matrix Matrix data
 * @param {number} size Matrix width
 */
function tick( matrix, size ) {

  const cellsRecords = []

  matrix.forEach( ( cellState, index ) => {

    // Set initial state if not defined
    if ( cellsRecords[ index ] === undefined ) {

      cellsRecords[ index ] = cellState

    }

    // Set others in previous left cell and update my others
    // Only when not on first column
    if ( index % size !== 0 ) {

      _updateCell( cellState, index, ( index - 1 ), cellsRecords )

    }

    // Set others in previous top cell
    _updateCell( cellState, index, ( index - size ), cellsRecords )

    // Set others in previous top left cell
    _updateCell( cellState, index, ( index - size - 1 ), cellsRecords )

    // Set others in previous top right cell
    _updateCell( cellState, index, ( index - size + 1 ), cellsRecords )

  } )

  function _updateCell( cellState, cellIndex, otherCellIndex, cellsRecords ) {

    if ( otherCellIndex >= 0 ) {

      // Increase current cell count
      if ( ( cellsRecords[ otherCellIndex ] % 2 ) !== 0 ) {

        cellsRecords[ cellIndex ] += 2

      }

      // Increase other cell count
      if ( cellState ) {

        cellsRecords[ otherCellIndex ] += 2

      }

    }

  }

  const nextMatrixState = cellsRecords.map( cell => {

    if (
      cell === 6 ||
      cell === 5 ||
      cell === 7
    ) {

      return 1

    }

    return 0

  } )

  return nextMatrixState

}

