/**
 * Run single period on given matrix data
 * @param {Array} matrix Matrix data
 * @param {number} size Matrix width
 */
function tick( matrix, size ) {

  const cellsRecords = []

  matrix.forEach( ( cellState, index ) => {

    // Set initial state if not defined
    if ( cellsRecords[ index ] === undefined ) {

      cellsRecords[ index ] = _setCellInitialState( cellState )

    }

    // Set others in previous left cell and update my others
    const prevCellIndex = ( index - 1 )

    _updateCell( cellState, index, prevCellIndex, cellsRecords )

    // Set others in previous top cell
    const topCellIndex = ( index - size )

    _updateCell( cellState, index, topCellIndex, cellsRecords )

    // Set others in previous top left cell
    const topLeftCellIndex = ( index - size - 1 )

    _updateCell( cellState, index, topLeftCellIndex, cellsRecords )

    // Set others in previous top right cell
    const topRightCellIndex = ( index - size + 1 )

    _updateCell( cellState, index, topRightCellIndex, cellsRecords )

  } )

  function _updateCell( cellState, cellIndex, otherCellIndex, cellsRecords ) {

    if ( otherCellIndex >= 0 ) {

      // cellsRecords[ otherCellIndex ].others += cellState * 2
      cellsRecords[ otherCellIndex ].others += cellState

      cellsRecords[ cellIndex ].others += cellsRecords[ otherCellIndex ].state

    }

  }


  const nextMatrixState = _tranformRecordsToMatrix( cellsRecords, size )

  return nextMatrixState

  function _tranformRecordsToMatrix( cellsRecords, size ) {

    const newMatrix = []

    cellsRecords.forEach( cellRecord => {

      let newCellState

      if (
        cellRecord.others === 3 ||
        (
          cellRecord.state === 1 &&
          cellRecord.others === 2
        )
      ) {

        newCellState = 1

      } else {

        newCellState = 0

      }

      newMatrix.push( newCellState )

    } )

    return newMatrix

  }

  function _setCellInitialState( state ) {

    return {
      state: state,
      others: 0
    }

  }

}

/**
 * Render life to string
 * @param {Array} matrix Matrix data
 * @param {number} size Matrix width
 * @return {string} String of life
 */
function render( matrix, size ) {

  let canvas = ``

  matrix.forEach( function( cellState, index ) {

    if (
      index > 0 &&
      index % size == 0
    ) {

      canvas += `\n`

    }

    canvas += ( cellState ? chalk.red( '🁢' ) : chalk.green( '🁢' ) )


  } )

  return canvas

}
